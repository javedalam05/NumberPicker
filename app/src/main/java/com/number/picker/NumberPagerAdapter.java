package com.number.picker;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

public class NumberPagerAdapter extends FragmentPagerAdapter {

    private List<NumberPickFragment> fragments;

    public NumberPagerAdapter(FragmentManager fm, List<NumberPickFragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return this.fragments.get(position);
    }

    @Override
    public int getCount() {
        return this.fragments.size();
    }
}